async function downloadAndSaveInvoice(url: string, filename: string): Promise<void> {
  try {
    const dir = FileSystem.documentDirectory;
    if (!dir) { Alert.alert('Error', 'Cannot access file system.'); return; }
    await FileSystem.downloadAsync(url, dir + filename);
    Alert.alert('Saved', `"${filename}" saved to your files.`);
  } catch (err) {
    console.error('downloadAndSaveInvoice failed:', err);
    Alert.alert('Error', 'Failed to download invoice.');
  }
}

async function shareInvoiceFile(url: string, filename: string): Promise<void> {
  try {
    const dir = FileSystem.cacheDirectory;
    if (!dir) { Alert.alert('Error', 'Cache directory unavailable.'); return; }
    await FileSystem.downloadAsync(url, dir + filename);
    const canShare = await Sharing.isAvailableAsync();
    if (canShare) await Sharing.shareAsync(dir + filename);
    else Alert.alert('Unavailable', 'Sharing is not supported on this device.');
  } catch (err) {
    console.error('shareInvoiceFile failed:', err);
    Alert.alert('Error', 'Failed to share invoice.');
  }
}

function openInvoiceActions(invoice: Invoice): void {
  if (!invoice.downloadURL) {
    Alert.alert(
      'Invoice Pending',
      'This invoice has been generated but the PDF is still being prepared. Please try again in a moment.',
    );
    return;
  }
  const filename = `invoice_${invoice.invoiceNumber || invoice.id}.pdf`;
  Alert.alert(
    `Invoice ${invoice.invoiceNumber || shortId(invoice.id)}`,
    'Download or share this invoice?',
    [
      { text: 'Download', onPress: () => downloadAndSaveInvoice(invoice.downloadURL!, filename) },
      { text: 'Share', onPress: () => shareInvoiceFile(invoice.downloadURL!, filename) },
      { text: 'Cancel', style: 'cancel' },
    ],
  );
}

