export default function InvoicesScreen() {
  const router = useRouter();
  const { user, loading: authLoading } = useAuth();
  const db = getFirestore();
  const uid = user?.uid;

  const [activeTab, setActiveTab] = useState<TabType>('orders');

  const [allOrders, setAllOrders] = useState<Order[]>([]);
  const [ordersLoading, setOrdersLoading] = useState(true);

  const [clientCustomOrders, setClientCustomOrders] = useState<ServiceOrderWithMeta[]>([]);
  const [customOrdersLoading, setCustomOrdersLoading] = useState(true);
  const [customOrderTraces, setCustomOrderTraces] = useState<Record<string, TraceStep[]>>({});
  const [expandedTraces, setExpandedTraces] = useState<Set<string>>(new Set());

  const [invoices, setInvoices] = useState<Invoice[]>([]);
  const [invLoading, setInvLoading] = useState(true);

  // Card expansion state per tab
  const [expandedCards, setExpandedCards] = useState<Set<string>>(new Set());
  const toggleCard = useCallback((id: string) => {
    setExpandedCards(prev => {
      const next = new Set(prev);
      next.has(id) ? next.delete(id) : next.add(id);
      return next;
    });
  }, []);

  const toggleTrace = useCallback((orderId: string) => {
    setExpandedTraces(prev => {
      const next = new Set(prev);
      next.has(orderId) ? next.delete(orderId) : next.add(orderId);
      return next;
    });
  }, []);

  // Dedup guard for auto-invoice creation within a single session.
  // Prevents the same order from being processed twice across snapshot re-fires.
  const autoInvoicedRef = useRef<Set<string>>(new Set());

  const [refreshing, setRefreshing] = useState(false);
  const onRefresh = useCallback(() => {
    setRefreshing(true);
    setTimeout(() => setRefreshing(false), 600);
  }, []);

  // ── Auth guard (no early return — gated render below) ──────────────────────
  useEffect(() => {
    if (authLoading) return;
    if (!uid?.trim()) router.replace('/auth');
  }, [authLoading, uid, router]);

  // ── 1. Orders (ticket + non-custom service) ────────────────────────────────
  useEffect(() => {
    if (!uid) return;
    let cancelled = false;
    setOrdersLoading(true);

    const q = query(
      collection(db, 'orders'),
      where('clientId', '==', uid),
      orderBy('createdAt', 'desc'),
    );

    const unsub = onSnapshot(
      q,
      async (snap) => {
        if (cancelled) return;
        const raw: Order[] = snap.docs.map((dc) => {
          const d = dc.data() as any;
          const orderType = (d.orderType || 'ticket') as 'ticket' | 'service';
          const ticketItems = Array.isArray(d.ticketQuantities)
            ? d.ticketQuantities.map((qty: any) => ({
                name: qty.type || 'Ticket',
                qty: Number(qty.quantity || 0),
                price: Number(qty.price || 0),
              }))
            : undefined;

          return {
            id: dc.id,
            title:
              d.title ??
              d.serviceName ??
              (orderType === 'ticket' ? d.ticketName : undefined) ??
              'Order',
            description:
              d.description ??
              d.specialRequests ??
              d.message ??
              '',
            status: (d.status || d.orderStatus || 'pending') as OrderStatus,
            orderType,
            amount: Number(d.amount ?? d.totalPrice ?? d.clientPrice ?? 0),
            currency: d.currency || 'MAD',
            clientId: d.clientId,
            artistId: d.artistId,
            cover:
              d.cover ||
              d.image ||
              d.coverImage ||
              d.imageUrl ||
              d.serviceCover ||
              d.serviceImage ||
              d.thumbnail,
            items: Array.isArray(d.items) ? d.items : ticketItems,
            paymentStatus: d.paymentStatus,
            paymentMethod: d.paymentMethod,
            notes: d.notes,
            createdAt: normalizeDate(d.createdAt),
          };
        });

        const namesMap = await fetchArtistNames(
          db,
          raw.map(o => o.artistId).filter(Boolean) as string[],
        );
        const enriched = raw.map(o => ({
          ...o,
          artistName: o.artistId ? namesMap[o.artistId] : undefined,
        }));

        if (cancelled) return;
        setAllOrders(enriched);
        setOrdersLoading(false);

        // Auto-invoice any newly-confirmed orders.
        for (const o of enriched) {
          if (!INVOICE_TRIGGER_STATUSES.includes(o.status)) continue;
          if (autoInvoicedRef.current.has(o.id)) continue;
          autoInvoicedRef.current.add(o.id);
          ensureInvoiceForOrder(db, {
            orderId: o.id,
            orderType: o.orderType,
            userId: uid,
            amount: o.amount,
            currency: o.currency,
            artistName: o.artistName,
            title: o.title,
          }).catch(err => console.warn('auto-invoice (order) failed:', err));
        }
      },
      (err) => {
        if (cancelled) return;
        console.error('Error loading orders:', err);
        setOrdersLoading(false);
      },
    );

    return () => { cancelled = true; unsub(); };
  }, [uid, db]);

  // ── 2. Custom orders ───────────────────────────────────────────────────────
  useEffect(() => {
    if (!uid) return;
    let cancelled = false;
    setCustomOrdersLoading(true);

    const unsub = getClientCustomOrdersRealtime(
      uid,
      async (rawOrders: CustomOrderField[]) => {
        if (cancelled) return;
        const artistIds = [...new Set(rawOrders.map(o => o.artistId).filter(Boolean) as string[])];
        const namesMap = await fetchArtistNames(db, artistIds);

        const enriched: ServiceOrderWithMeta[] = rawOrders.map((o: any) => ({
          ...o,
          status: o.status || o.orderStatus || 'pending',
          createdAt: normalizeDate(o.createdAt),
          artistName: o.artistId ? namesMap[o.artistId] : undefined,
        }));

        if (cancelled) return;
        setClientCustomOrders(enriched);
        setCustomOrdersLoading(false);

        // Auto-invoice confirmed custom orders.
        for (const o of enriched) {
          const status = ((o as any).status || 'pending') as OrderStatus;
          if (!INVOICE_TRIGGER_STATUSES.includes(status)) continue;
          if (autoInvoicedRef.current.has(o.id)) continue;
          autoInvoicedRef.current.add(o.id);
          ensureInvoiceForOrder(db, {
            orderId: o.id,
            orderType: 'service',
            userId: uid,
            amount: Number((o as any).clientPrice ?? 0),
            currency: 'MAD',
            artistName: o.artistName,
            title: (o as any).serviceName || 'Service',
          }).catch(err => console.warn('auto-invoice (custom) failed:', err));
        }
      },
      (err) => {
        if (cancelled) return;
        console.error('Error loading custom orders:', err);
        setCustomOrdersLoading(false);
      },
    );

    return () => { cancelled = true; unsub(); };
  }, [uid, db]);

  // ── 3. Per-custom-order traceability ───────────────────────────────────────
  useEffect(() => {
    if (!uid || !clientCustomOrders.length) {
      setCustomOrderTraces({});
      return;
    }
    let cancelled = false;
    (async () => {
      const tracesMap: Record<string, TraceStep[]> = {};
      for (const co of clientCustomOrders) {
        try {
          tracesMap[co.id] = await getOrderTraceability(db, uid, co.id);
        } catch {
          tracesMap[co.id] = [];
        }
      }
      if (!cancelled) setCustomOrderTraces(tracesMap);
    })();
    return () => { cancelled = true; };
  }, [clientCustomOrders, uid, db]);

  // ── 4. Invoices ────────────────────────────────────────────────────────────
  useEffect(() => {
    if (!uid) return;
    setInvLoading(true);

    const q = query(
      collection(db, 'invoices'),
      where('userId', '==', uid),
      orderBy('createdAt', 'desc'),
    );
    const unsub = onSnapshot(
      q,
      (snap) => {
        const data: Invoice[] = snap.docs.map((d) => {
          const x = d.data() as any;
          return {
            id: d.id,
            orderId: x.orderId,
            orderType: x.orderType || 'service',
            userId: x.userId,
            downloadURL: x.downloadURL,
            createdAt: normalizeDate(x.createdAt),
            amount: Number(x.amount ?? 0),
            currency: x.currency || 'MAD',
            status: x.status || 'issued',
            autoGenerated: !!x.autoGenerated,
            invoiceNumber: x.invoiceNumber,
            artistName: x.artistName,
            title: x.title,
          };
        });
        setInvoices(data);
        setInvLoading(false);
      },
      (err) => {
        console.error('Error loading invoices:', err);
        setInvLoading(false);
      },
    );
    return () => unsub();
  }, [uid, db]);

  // ── Delete handlers ────────────────────────────────────────────────────────
  const confirmDeleteOrder = useCallback((orderId: string, isCustom = false) => {
    Alert.alert(
      'Delete Order',
      'This action cannot be undone. Are you sure?',
      [
        { text: 'Cancel', style: 'cancel' },
        {
          text: 'Delete',
          style: 'destructive',
          onPress: async () => {
            try {
              await deleteDoc(doc(db, isCustom ? 'customOrders' : 'orders', orderId));
            } catch (err) {
              console.error('delete failed:', err);
              Alert.alert('Error', 'Unable to delete this order.');
            }
          },
        },
      ],
    );
  }, [db]);

  // ── Summary stats (derived) ───────────────────────────────────────────────
  const stats = useMemo(() => {
    const allItems = [
      ...allOrders.map(o => ({ amount: o.amount, status: o.status })),
      ...clientCustomOrders.map((o: any) => ({
        amount: Number(o.clientPrice ?? 0),
        status: (o.status || o.orderStatus || 'pending') as OrderStatus,
      })),
    ];
    const completedOrConfirmed = allItems.filter(i =>
      i.status === 'completed' || i.status === 'confirmed' || i.status === 'accepted',
    );
    return {
      totalSpent: completedOrConfirmed.reduce((s, i) => s + (i.amount || 0), 0),
      pendingCount: allItems.filter(i => i.status === 'pending').length,
      completedCount: completedOrConfirmed.length,
    };
  }, [allOrders, clientCustomOrders]);

  // ── Render ────────────────────────────────────────────────────────────────
  if (authLoading || !uid) {
    return (
      <View style={[styles.container, styles.loadingContainer]}>
        <ActivityIndicator size="large" color="#6366f1" />
        <Text style={styles.loadingText}>Loading…</Text>
      </View>
    );
  }

  const emptyState = (icon: string, title: string, body: string) => (
    <View style={styles.emptyContainer}>
      <Text style={styles.emptyIcon}>{icon}</Text>
      <Text style={styles.emptyTitle}>{title}</Text>
      <Text style={styles.emptyText}>{body}</Text>
    </View>
  );

  const tabs: Array<{ key: TabType; icon: keyof typeof Ionicons.glyphMap; label: string; count: number }> = [
    { key: 'orders', icon: 'cart-outline', label: 'Orders', count: allOrders.length },
    { key: 'custom', icon: 'construct-outline', label: 'Custom', count: clientCustomOrders.length },
    { key: 'invoices', icon: 'document-text-outline', label: 'Invoices', count: invoices.length },
  ];

  return (
    <View style={styles.container}>
      <SummaryHeader
        totalSpent={stats.totalSpent}
        pendingCount={stats.pendingCount}
        completedCount={stats.completedCount}
      />

      <View style={styles.tabRow}>
        {tabs.map(({ key, icon, label, count }) => {
          const focused = activeTab === key;
          return (
            <TouchableOpacity
              key={key}
              style={[styles.tabButton, focused && styles.tabButtonActive]}
              onPress={() => setActiveTab(key)}
              activeOpacity={0.8}
            >
              <Ionicons name={icon} size={15} color={focused ? '#fff' : '#6366f1'} />
              <Text style={[styles.tabLabel, focused && styles.tabLabelActive]}>
                {label}
              </Text>
              {count > 0 ? (
                <View style={[styles.tabBadge, focused && { backgroundColor: '#ffffff' }]}>
                  <Text style={[styles.tabBadgeText, focused && styles.tabBadgeTextActive]}>
                    {count}
                  </Text>
                </View>
              ) : null}
            </TouchableOpacity>
          );
        })}
      </View>

      {/* TAB: Orders */}
      {activeTab === 'orders' && (
        ordersLoading ? (
          <View style={styles.loadingContainer}>
            <ActivityIndicator size="large" color="#6366f1" />
            <Text style={styles.loadingText}>Loading orders…</Text>
          </View>
        ) : (
          <FlatList
            data={allOrders}
            keyExtractor={(item) => item.id}
            renderItem={({ item }) => (
              <OrderCard
                item={item}
                onDelete={() => confirmDeleteOrder(item.id)}
                expanded={expandedCards.has(item.id)}
                onToggleExpand={() => toggleCard(item.id)}
              />
            )}
            contentContainerStyle={styles.listContainer}
            showsVerticalScrollIndicator={false}
            refreshControl={<RefreshControl refreshing={refreshing} onRefresh={onRefresh} tintColor="#6366f1" />}
            ListEmptyComponent={emptyState(
              '📭',
              'No Orders Yet',
              'Your ticket and service orders will appear here once placed.',
            )}
          />
        )
      )}

      {/* TAB: Custom Orders */}
      {activeTab === 'custom' && (
        customOrdersLoading ? (
          <View style={styles.loadingContainer}>
            <ActivityIndicator size="large" color="#6366f1" />
            <Text style={styles.loadingText}>Loading custom orders…</Text>
          </View>
        ) : (
          <FlatList
            data={clientCustomOrders}
            keyExtractor={(item) => item.id}
            renderItem={({ item }) => (
              <CustomOrderCard
                item={item}
                traceSteps={customOrderTraces[item.id] || []}
                onToggleTrace={toggleTrace}
                onDelete={() => confirmDeleteOrder(item.id, true)}
                showTrace={expandedTraces.has(item.id)}
                expanded={expandedCards.has(item.id)}
                onToggleExpand={() => toggleCard(item.id)}
              />
            )}
            contentContainerStyle={styles.listContainer}
            showsVerticalScrollIndicator={false}
            refreshControl={<RefreshControl refreshing={refreshing} onRefresh={onRefresh} tintColor="#6366f1" />}
            ListEmptyComponent={emptyState(
              '📦',
              'No Custom Orders',
              'Custom service requests you place with artists will appear here with full traceability.',
            )}
          />
        )
      )}

      {/* TAB: Invoices */}
      {activeTab === 'invoices' && (
        invLoading ? (
          <View style={styles.loadingContainer}>
            <ActivityIndicator size="large" color="#6366f1" />
            <Text style={styles.loadingText}>Loading invoices…</Text>
          </View>
        ) : (
          <FlatList
            data={invoices}
            keyExtractor={(item) => item.id}
            renderItem={({ item }) => (
              <InvoiceItem
                item={item}
                expanded={expandedCards.has(item.id)}
                onToggleExpand={() => toggleCard(item.id)}
              />
            )}
            contentContainerStyle={styles.listContainer}
            showsVerticalScrollIndicator={false}
            refreshControl={<RefreshControl refreshing={refreshing} onRefresh={onRefresh} tintColor="#6366f1" />}
            ListEmptyComponent={emptyState(
              '🧾',
              'No Invoices Yet',
              'Invoices are generated automatically when an order is confirmed or completed.',
            )}
          />
        )
      )}
    </View>
  );
}