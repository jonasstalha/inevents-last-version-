function formatLongDate(iso: string): string {
  return new Date(iso).toLocaleDateString('en-US', {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
    year: 'numeric',
  });
}

function formatShortDate(iso: string): string {
  return new Date(iso).toLocaleDateString('en-US', {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  });
}

function shortId(id: string): string {
  return id.substring(0, 8).toUpperCase();
}

async function fetchArtistNames(
  db: Firestore,
  artistIds: string[],
): Promise<Record<string, string>> {
  const uniqueIds = [...new Set(artistIds.filter(Boolean))];
  if (!uniqueIds.length) return {};

  const map: Record<string, string> = {};
  try {
    const snap = await getDocs(collection(db, 'users'));
    for (const dc of snap.docs) {
      const d = dc.data() as any;
      if (d.role === 'artist' || d.type === 'artist') {
        map[dc.id] = d.storeName || d.name || d.displayName || `Artist ${dc.id.slice(0, 6)}`;
      }
    }
  } catch (err) {
    console.warn('fetchArtistNames: failed to list users', err);
  }
  for (const uid of uniqueIds) {
    if (!map[uid]) map[uid] = `Artist ${uid.slice(0, 6)}`;
  }
  return map;
}

/**
 * Creates an invoice for an order, but only if one doesn't already exist.
 *
 * IMPORTANT: this is a client-side stop-gap. The proper place for this is a
 * Cloud Function triggered on `orders/{orderId}` status change — that's the
 * only way to guarantee single-source-of-truth invoice generation, generate
 * a real PDF, and avoid race conditions between multiple online clients.
 */
async function ensureInvoiceForOrder(
  db: Firestore,
  params: {
    orderId: string;
    orderType: 'ticket' | 'service';
    userId: string;
    amount: number;
    currency: string;
    artistName?: string;
    title?: string;
  },
): Promise<{ created: boolean; invoiceId?: string }> {
  if (!params.userId || !params.orderId) return { created: false };
  if (!params.amount || params.amount <= 0) return { created: false };

  try {
    // Dedup guard: bail if any invoice already references this order.
    const existing = await getDocs(
      query(
        collection(db, 'invoices'),
        where('orderId', '==', params.orderId),
        limit(1),
      ),
    );
    if (!existing.empty) return { created: false, invoiceId: existing.docs[0].id };

    const invoiceNumber = `INV-${Date.now().toString(36).toUpperCase()}-${shortId(params.orderId)}`;
    const ref = await addDoc(collection(db, 'invoices'), {
      orderId: params.orderId,
      orderType: params.orderType,
      userId: params.userId,
      amount: params.amount,
      currency: params.currency || 'MAD',
      status: 'issued',
      invoiceNumber,
      title: params.title || '',
      artistName: params.artistName || '',
      autoGenerated: true,
      // downloadURL intentionally omitted — populated by a server function
      // that renders the PDF and uploads it to Firebase Storage.
      downloadURL: '',
      createdAt: serverTimestamp(),
    });
    return { created: true, invoiceId: ref.id };
  } catch (err) {
    console.error('ensureInvoiceForOrder failed:', err);
    return { created: false };
  }
}

function buildTimeline(
  steps: TraceStep[],
  orderCreatedAt: string,
): Array<{ date: string; label: string; artistName: string; time: string; color: string }> {
  const seen = new Set<string>();
  const timeline: Array<{ date: string; label: string; artistName: string; time: string; color: string }> = [];

  const push = (d: { date: string; label: string; artistName: string; color: string }) => {
    const key = `${d.date}|${d.label}|${d.artistName}`;
    if (!seen.has(key)) {
      seen.add(key);
      timeline.push({ ...d, time: '' });
    }
  };

  push({
    date: formatLongDate(orderCreatedAt),
    label: 'Order placed',
    artistName: 'You',
    color: '#6366f1',
  });

  for (const s of steps) {
    const status = (s.status as OrderStatus) in STATUS_COLOR ? (s.status as OrderStatus) : 'pending';
    push({
      date: formatLongDate(s.createdAt),
      label: s.displayLabel || STATUS_LABEL[status],
      artistName: s.artistName,
      color: STATUS_COLOR[status],
    });
    timeline[timeline.length - 1].time = timeAgo(s.createdAt);
  }

  return timeline;
}

